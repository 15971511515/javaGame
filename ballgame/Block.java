package cn.ballgame;

public class Block {
	int bx;
	int by;
	int bw;
	int bh;
	int hp;
	/**
	 * @param bx
	 * @param by
	 * @param bw
	 * @param bh
	 * @param hp
	 */
	Block(int bx, int by, int bw, int bh, int hp) {
		super();
		this.bx = bx;
		this.by = by;
		this.bw = bw;
		this.bh = bh;
		this.hp = hp;
	}


}
